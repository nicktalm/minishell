#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*newstr;

	i = 0;
	len = ft_strlen(s);
	newstr = malloc((len + 1) * sizeof(char));
	if (!(newstr))
		return (NULL);
	while (s[i] != '\0')
	{
		newstr[i] = f(i, s[i]);
		i++;
	}
	newstr[i] = '\0';
	return (newstr);
}
