#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	unsigned char	*cpystr;
	size_t			i;

	cpystr = str;
	i = 0;
	while (n > i)
	{
		cpystr[i] = 0;
		i++;
	}
}
